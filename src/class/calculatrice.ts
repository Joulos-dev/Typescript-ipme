export class Calculatrice {




    add(nb1:number, nb2:number){
        console.log(nb1 + nb2);
    }
    subtract(nb1:number, nb2:number){
        console.log(nb1 - nb2);
    }
    multiply(nb1:number, nb2:number){
        console.log(nb1 * nb2);
    }
    divide(nb1:number, nb2:number){
        console.log(nb1 / nb2);
    }

}